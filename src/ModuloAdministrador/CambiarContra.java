    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String Instruccion = "";
            String contra1 = "", contra2 = "", contra3 = "";
            contra1 = Actual.getText();
            contra2 = Nuevo1.getText();
            contra3 = Nuevo2.getText();
            if(contra1.equals(User.getString("Contrasenia")) == true){
                if(contra2.equals(contra3)){
                    //vamos a cambiar la contra
                    Instruccion = "UPDATE usuarios SET usuarios.Contrasenia = '" + contra2 + "'WHERE usuarios.NombreUsuario = '" + User.getString("NombreUsuario") + "';";
                    int  a;
                    try (PreparedStatement pst = conexcion.prepareStatement(Instruccion)) {
                        a = pst.executeUpdate();
                        if (a>0){
                            System.out.println("Guardado");
                            JOptionPane.showMessageDialog(null, "¡Se ha cambiado la contraseña de " + User.getString("NombreUsuario"));
                            Ventanita.setEnabled(true);
                            this.dispose();
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(CrearAdmin.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                else{
                    JOptionPane.showMessageDialog(null, "Las contraseñas no coindicen");
                }
            }
            else{
                JOptionPane.showMessageDialog(null, "Las contraseñas no coindicen");
            }
        } catch (SQLException ex) {
            Logger.getLogger(CambiarContra.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

