    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String ciclo = Nombre.getText().trim();
            Statement b = base.createStatement();
            ResultSet consultab = b.executeQuery("SELECT Id FROM cicloescolar WHERE cicloescolar.Anio = '"+ciclo+"';");
            if(!consultab.next()){
            String ciclo_nuevo;
            String instruccion_asignacion = "INSERT INTO cicloescolar(Anio,Listo) VALUES ('"+ciclo+"',0);";
            PreparedStatement pst = base.prepareStatement(instruccion_asignacion);
            pst.executeUpdate();
             //Obtengo el Id del nuevo ciclo;
            Statement a = base.createStatement();
            ResultSet consulta = a.executeQuery("SELECT Id FROM cicloescolar WHERE cicloescolar.Anio = '"+ciclo+"';");
            consulta.next();
            ciclo_nuevo = consulta.getString(1);
            
            String[] opciones = new String[2];
            opciones[0] = "SI";
            opciones[1] = "NO";
            
            //Pregunto si desea copiar los cursos y grados de un ciclo anterior
            int eleccion = JOptionPane.showOptionDialog(null, "Desea exportar datos de un ciclo anteriror?", "Contenido Ciclo Escolar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, opciones, opciones[0]);
            //Si la respuesta es si
            if(eleccion == JOptionPane.YES_OPTION){  
                String ciclo_copiar;
                this.dispose();
                new Importar_datos(new Frame(), true, base, ciclo_nuevo).show();
            }
            //Si la respuesta es no
            else{
                this.dispose();
                
            }
            }else{
                JOptionPane.showMessageDialog(this, "Ya existe un ciclo con el mismo nombre", "ERROR", JOptionPane.ERROR_MESSAGE, null);
   
            }
        } catch (SQLException ex) {
            Logger.getLogger(Crear_Ciclo_Escolar_1.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

