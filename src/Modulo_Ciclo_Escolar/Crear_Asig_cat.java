    public Crear_Asig_cat(java.awt.Frame parent, boolean modal,Connection base,String ciclo,String Id_grado,String grado) {
        super(parent, modal);
            initComponents();
            this.base = base;
            this.ciclo = ciclo;
            this.Id_grado = Id_grado;
            this.grado.setText(grado);
            posicion_curso =0;
            posicion_cat =0;
            Id_Curso = new ArrayList<String>();
            Id_cat = new ArrayList<String>();
        try { 
            //obtengo todos los cursos que corresponden al a√±o seleccionado
            Statement a = base.createStatement();
            ResultSet consulta = a.executeQuery("SELECT  curso.Id, curso.Nombre  FROM asignacioncat INNER JOIN cicloescolar ON asignacioncat.CicloEscolar_Id = cicloescolar.Id INNER JOIN curso ON asignacioncat.Curso_Id = Curso.Id WHERE asignacioncat.CicloEscolar_Id = "+ciclo+" AND asignacioncat.Grado_Id is null AND asignacioncat.Catedratico_Id is null;");
            while(consulta.next()){
                String Id = consulta.getString(1);
                String Nombre = consulta.getString(2);
                Id_Curso.add(Id);
                curso.addItem(Nombre);
            }
            //Obtengo todos los catedraticos existentes
            a = base.createStatement();
            consulta = a.executeQuery("SELECT catedratico.Id, catedratico.Nombres, catedratico.Apellidos FROM catedratico");
            while(consulta.next()){
                String Id = consulta.getString(1);
                String Nombre = consulta.getString(2)+" "+consulta.getString(3);
                Id_cat.add(Id);
                catedratico.addItem(Nombre);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Crear_Asig_cat.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }

