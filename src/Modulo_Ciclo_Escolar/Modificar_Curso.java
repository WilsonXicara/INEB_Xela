    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Nombre = Tx_Nombre.getText();
        if(Nombre.length()!=0){
            String instruccion;
            if(posicion_cat!=0){             
                instruccion = "UPDATE curso SET Nombre = '"+Nombre+"', curso.`Catedrático_Id` ="+ Id_catedratico.get(posicion_cat)+"  WHERE Id = "+Id_curso.get(posicion_curso);
            }
            else{
                instruccion = "UPDATE curso SET Nombre = '"+Nombre+"', curso.`Catedrático_Id` =null  WHERE Id = "+Id_curso.get(posicion_curso);
            }
            try {
                PreparedStatement pst = base.prepareStatement(instruccion);
                int a = pst.executeUpdate();
                if (a>0){
                    System.out.println("Guardado");
                }
                this.dispose();
            } catch (SQLException ex) {
                Logger.getLogger(Crear_Grado.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Debe llenar todos los campos", "ERROR", JOptionPane.ERROR_MESSAGE, null);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

