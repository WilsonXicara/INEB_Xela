    private void tx_cursoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_tx_cursoItemStateChanged
        posicion_curso = tx_curso.getSelectedIndex();
        Statement a;
        try {
            a = base.createStatement();
            ResultSet consulta = a.executeQuery("SELECT catedratico.Id FROM componentes INNER JOIN catedratico ON componentes. = catedratico.Id WHERE curso.Id ="+Id_curso.get(posicion_curso) );
            if(consulta.next()){
                String cat = consulta.getString(1);
                int aux =  Id_catedratico.indexOf(cat);
                tx_catedratico.setSelectedIndex(aux);
                
            }
            else{
                tx_catedratico.setSelectedIndex(0);
                
            }
            Tx_Nombre.setText((String)tx_curso.getSelectedItem());
            
        } catch (SQLException ex) {
            Logger.getLogger(Modificar_Curso.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_tx_cursoItemStateChanged

